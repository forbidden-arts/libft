#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sum;
	int	sign;

	i = 0;
	sum = 0;
	sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
		sign = 1 - 2 * (str[i++] == '-');
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (sum == INT_MAX / 10 && str[i] == '8' && sign == -1
			&& !ft_isdigit(str[i + 1]))
			return (INT_MIN);
		sum = 10 * sum + (str[i++] - '0');
	}
	return (sum * sign);
}
